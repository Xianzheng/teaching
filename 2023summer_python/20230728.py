python = {'a','b','c'}

math = {'c','d'}

# intersection part
print(python.intersection(math))

print(python & math)

# union part
print(math.union(python))

print(python | math)

print(python - math)

print(math - python)

# & -> intersection
# | -> union
# - -> only exit in

